#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		c;
	size_t	i;

	c = 0;
	i = 0;
	while (s1[c])
		c++;
	while (s2[i] && i < n)
	{
		s1[c] = s2[i];
		c++;
		i++;
	}
	s1[c] = '\0';
	return (s1);
}

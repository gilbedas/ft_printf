#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	c;
	int i;

	c = 0;
	i = 0;
	while (s1[c])
		c++;
	while (s2[i])
	{
		s1[c] = s2[i];
		c++;
		i++;
	}
	s1[c] = s2[i];
	return (s1);
}
